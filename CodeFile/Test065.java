// 반복문(for문) 실습
// 다중 for문(반복문의 중첩)을 활용한 별찍기 실습
// 별찍기 실습
//-------------------------------------------------------------------------------------------------
// ● 과제
//    다음과 같은 내용이 출력될 수 있도록
//    반복문의 중첩 구문을 작성하여 프로그램을 구현한다.
//-------------------------------------------------------------------------------------------------
// 실행 예 및 분석
/*
    
*********  --> 라인4 / 공백0 / 별9 == 2*4+1
 *******   --> 라인3 / 공백1 / 별7 == 2*3+1
  *****    --> 라인2 / 공백2 / 별5 == 2*2+1
   ***     --> 라인1 / 공백3 / 별3 == 2*1+1
    *      --> 라인0 / 공백4 / 별1 == 2*0+1

*/
//-------------------------------------------------------------------------------------------------
public class Test065
{
	public static void main(String[] args)
	{
		for (int line=4; line>=0; line--) // line 총 5줄 (4-->0)
		{
			// 공백은 밑으로 갈수록 누적 / (첫 줄은 X),4,43,432,4321
			for (int blank=4; blank>line; blank--)
			{
				System.out.print(" ");
			}
			// 별은 2*n+1, n은 4에서 1로 감소(n==line)
			for (int star=1; star<=2*line+1; star++)
			{
				System.out.print("*");
			}
			System.out.println();
		}		
	}
}
