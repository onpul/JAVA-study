// 다중 for문(반복문의 중첩)을 활용한 별찍기 실습
// 별찍기 실습
//-------------------------------------------------------------------------------------------------
// ● 과제
//    다음과 같은 내용이 출력될 수 있도록
//    반복문의 중첩 구문을 작성하여 프로그램을 구현한다.
//-------------------------------------------------------------------------------------------------
/* 
실행 예 / 분석
---------------------------------------------------------------------------------------------------
 실행 예    별  공백     별 위치            구성 
---------------------------------------------------------------------------------------------------
	*        1     4    000010000   1234*6789 -->     5
   ***       3     3    000111000   123***789 -->    456
  *****      5     2    001111100   12*****89 -->   34567
 *******     7     1    011111110   1*******9 -->  2345678
*********    9     0    111111111   ********* --> 123456789 
 *******     7     1    011111110
  *****      5     2    001111100 
   ***       3     3    000111000
    *        1     4    000010000
*/
//-------------------------------------------------------------------------------------------------
public class Test066
{
	public static void main(String[] args)
	{
		// 1. 공백 감소, 별 증가
		for (int line=0; line<=4; line++) // 총 5줄
		{
			for (int blank=4; blank>line; blank--) // 줄,공백 / 1,4321 2,432 3,43 2,4 1,(마지막 줄X)
			{
				System.out.print(" ");
			}
			for (int star=0; star<2*line+1; star++) // 별은 2*line+1 (line값 증가-->별 증가)
			{
				System.out.print("*");
			}
			System.out.println();
		}
		// 2. 공백 증가, 별 감소 (065랑 다른 점--> 별이 7531)
		for (int line=3; line>=0; line--) // line 총 4줄 (3-->0)
		{	
			for (int blank=4; blank>line; blank--) // 공백은 밑으로 갈수록 누적 / (첫 줄은 X),4,43,432,4321
			{
				System.out.print(" ");
			}	
			for (int star=1; star<=2*line+1; star++)// 별은 2*n+1, n은 3에서 1로 감소(n==line)
			{
				System.out.print("*");
			}
			System.out.println();
		}		
	}
}
